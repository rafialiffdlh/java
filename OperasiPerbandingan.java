public class OperasiPerbandingan {
    public static void main(String[] args) {
        int value1 = 100;
        int value2 = 100;
        int value3 = 300;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 > value3);
        System.out.println(value1 < value3);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 >= value3);
        System.out.println(value1 <= value3);
        
    }
}
