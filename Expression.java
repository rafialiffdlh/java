public class Expression {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;

        int value;
        value = 10;

        
        System.out.println(value);

        System.out.println(value = 100);

        System.out.println(a + b + c);
        System.out.println(a - b - c);
        System.out.println(a * b * c);
        System.out.println(a / b / c);
        System.out.println(a % b % c);
    }
    
}
