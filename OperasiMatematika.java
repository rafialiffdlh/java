public class OperasiMatematika {
    public static void main(String[] args){
        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int c = 100;

    c += 10; 
    System.out.println(c);
    // hasilnya 110

    c -= 10;
    System.out.println(c);
    // hasilnya 110 - 10 = 100

    c *= 10;
    System.out.println(c);
    // hasilnya 100 * 10 = 1000

    int d = 100;

    d++;
    System.out.println(d);

    d--;
    System.out.println(d);
    }
}
